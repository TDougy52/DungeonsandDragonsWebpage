<p>This is the first part in making your blood thirsty character. You must roll some dice to get your ability scores. There are a couple methods to use.</p>
<table>
<tr>
<th>Method</th> <th>Number of Dice</th> <th>Resultant</th>
</tr>
<tr>
<td>1</td> <td>3</td> <td>Sum the rolled dice and use that as your ability score.</td>
</tr>
<tr>
<td>2</td> <td>4</td> <td>Drop the lowest roll then sum the other three rolled dice and use that as your ability score.</td>
</tr>
</table>
<p>People usually use the second method to generate their ability scores. The dice used will be six-sided dice. If you do not have dice availible to you, you may use a online dice roller.</p>
<p>Here is a link to a <a href="https://www.random.org/dice/?num=4" target="_blank"> Dice-Roller </a>.
<p>Once you roll the dice and get your 6 numbers you are then to place the numbers into the Ability Scores table on the Character sheet. As shown in the Picture below. </p>
<img src="images/Scores.jpg" alt="Before and After" height="165" width="413">
<p>The character sheet pdf will already calculate the ability modifiers once you insert the values you have rolled in each box. You may choose where each number is placed (Unless your Dungeon Master says different). 
Keep in mind that each class (which you will choose later in a few minutes) use different ability scores. Say that after you look through the classes and you want to be a Fighter. Fighters main stats are Strength (Str) 
and Constitution (Con) but they will also want a high Dexterity (Dex).
If you want to change your scores make sure you ask your Dungeon Master (DM) first before going ahead and changing them.</p>
